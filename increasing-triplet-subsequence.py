"""
Given an integer array nums, return true if there exists a triple of indices
(i, j, k) such that i < j < k and nums[i] < nums[j] < nums[k].

If no such indices exists, return false.
"""

from typing import List


class Solution:
    def increasingTriplet(self, nums: List[int]) -> bool:
        exist = False

        # max_suffix = [0] * len(nums)
        # max_suffix[-1] = nums[-1]
        # for i in range(len(nums) - 2, -1, -1):
        #     max_suffix[i] = max(max_suffix[i + 1], nums[i])
        #
        # for i in range(len(nums) - 2):
        #     if nums[i] < max_suffix[i + 1]:
        #         for j in range(i + 1, len(nums) - 1):
        #             if nums[j] > nums[i] and nums[j] < max_suffix[j + 1]:
        #                 exist = True
        #                 break
        #         if exist:
        #             break

        # let's implement the above solution in a more optimized way
        # we can keep track of the minimum and second minimum element
        # and if we find a number greater than both, we can return true
        # this is because the second minimum element is the smallest number
        # that is greater than the minimum element
        # and if we find a number greater than both, we have found the third element
        # that is greater than the first two elements
        # this is a more optimized solution with O(n) time complexity
        # and O(1) space complexity

        first_min = float("inf")
        second_min = float("inf")

        for num in nums:
            if num <= first_min:
                first_min = num
            elif num <= second_min:
                second_min = num
            else:
                exist = True
                break

        return exist


